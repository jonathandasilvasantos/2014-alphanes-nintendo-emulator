package cpu

func Branch(cpu *CPU, value uint16) {

    if (cpu.PC & 0xFF00) != (cpu.PC & 0xFF00) {
	cpu.CYCSpecial+=2
    } else { cpu.CYCSpecial++ }
    cpu.PC = value
}
